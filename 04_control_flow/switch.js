const month = "march";
switch(month){
    case "jan":
        console.log("January");
        break;
    case "fab":
        console.log("fabruary");
        break;
    case "march":
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;
    case"june":
        console.log("June");
        break;
  
        default:
            console.log("default case match");
            break;
        
}